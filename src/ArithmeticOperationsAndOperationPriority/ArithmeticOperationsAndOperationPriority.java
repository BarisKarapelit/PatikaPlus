package ArithmeticOperationsAndOperationPriority;

public class ArithmeticOperationsAndOperationPriority {
    public static void main(String[] args) {
        // Arithmetic operations and operation priority
        int a = 5;
        int b = 2;
        int c = 10;
        int d = 5;
        int e = 2;
        int f = 8;
        int g = 2;
        int h = 2;
        int i = 2;
        int j = 2;
        int k = 2;
        int l = 2;
        int m = 2;
        int n = 2;
        int o = 2;
        int p = 2;
        int q = 2;
        int r = 2;
        int s = 2;
        int t = 2;
        int u = 2;
        int v = 2;
        int w = 2;
        int x = 2;
        int y = 2;
        int z = 2;

        int result = a + b - c * d / e % f + g - h * i / j % k + l - m * n / o % p + q - r * s / t % u + v - w * x / y % z;
        // 5 + 2 - 10 * 5 / 2 % 8
        System.out.println(result);
    }
}
