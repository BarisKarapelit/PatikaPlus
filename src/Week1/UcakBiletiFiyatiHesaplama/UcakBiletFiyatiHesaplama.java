package Week1.UcakBiletiFiyatiHesaplama;

public class UcakBiletFiyatiHesaplama {
    private static int yas;
    private static boolean yolculukTipi;
    private static int km;
    private static double fiyat;

    public int getYas() {
        return yas;
    }
    public void setYas(int yas) {
        this.yas = yas;
    }
    public boolean isYolculukTipi() {
        return yolculukTipi;
    }
    public void setYolculukTipi(boolean yolculukTipi) {
        this.yolculukTipi = yolculukTipi;
    }
    public int getKm() {
        return km;
    }
    public void setKm(int km) {
        this.km = km;
    }
    public double getFiyat() {
        return fiyat;
    }
    public void setFiyat(double fiyat) {
        this.fiyat = fiyat;
    }
}
